#! /usr/bin/env python3

###############################################################################
# Imports                                                                     #
###############################################################################

import subprocess


###############################################################################
# Constants                                                                   #
###############################################################################

PPAS = [
    "ppa:webupd8team/sublime-text-3",
    "ppa:webupd8team/java"
]


###############################################################################
# Helpers                                                                           #
###############################################################################

def add_ppa(ppa):
    subprocess.call(["sudo",
                     "add-apt-repository",
                     ppa])


###############################################################################
# Main script                                                                 #
###############################################################################

def main():

    map(add_ppa, PPAS)


if __name__ == '__main__':
    main()
